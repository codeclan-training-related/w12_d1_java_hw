public class Calculator {

//methods
    public int addNums( int numOne, int numtwo){
        return numOne+numtwo;
    }
    public int subtractNums( int numOne, int numtwo){
        return numOne-numtwo;
    }

    public int multiplyNums( int numOne, int numtwo){
        return numOne * numtwo;
    }

    public double divideNums( double numOne, double numtwo){
        return numOne / numtwo;
    }

}
